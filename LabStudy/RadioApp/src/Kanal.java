public class Kanal {
    private String kanalAdi;
    private String kanalLinki;
    public Kanal(String kanalAdi, String kanalLinki) {
        this.kanalAdi = kanalAdi;
        this.kanalLinki = kanalLinki;
    }
    public String getKanalAdi() {
        return kanalAdi;
    }
    public void setKanalAdi(String kanalAdi) {
        this.kanalAdi = kanalAdi;
    }
    public String getKanalLinki() {
        return kanalLinki;
    }
    public void setKanalLinki(String kanalLinki) {
        this.kanalLinki = kanalLinki;
    }

}
